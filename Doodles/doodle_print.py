#print() 101
print("Hello world!")

#Normalweise am Ende newline eingefügt, kann durch end= modifiziert werden
print("!dlrow olleH", end=" ")

#Jeder string durch Leerzeichen separiert
print("Hello", "world", "!", "!", "dlrow", "olleH")

#Jeder String kann in neuer Zeile stehen
print("Hello",
      "world!",
      "!dlrow",
      "olleH")

#Triple-Quotes printen genau wie geschrieben
print(
    """
             W
             T
             F
                      L O
                L""")

#Quotes innerhalb eines Strings müssen die anderen Quotes sein
print("Careful aboth those 'quotes'!")
